#np.linspace() can be used to create a sequence of numbers
import numpy as np
seq1=np.linspace(0,10,11)
print('Using np.linspace to produce a sequence:\n', seq1)

#function np.arange returns a sequence of numbers
seq2=np.arange(0,10)
print("The function np.arange returns a sequence of numbers\n",seq2)

